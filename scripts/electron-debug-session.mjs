import { _electron as electron } from 'playwright'
import fsSync from 'node:fs'
import fs from 'node:fs/promises'
import path from 'node:path'

const rootDir = path.resolve(new URL('..', import.meta.url).pathname.slice(process.platform === 'win32' ? 1 : 0))
const mainEntry = path.join(rootDir, 'dist-electron', 'main', 'index.js')
const rendererEntry = path.join(rootDir, 'dist', 'index.html')
const artifactRoot = path.resolve(process.env.ARYN_ELECTRON_DEBUG_ARTIFACT_ROOT ?? path.join(rootDir, 'tmp', 'electron-debug-session'))
const runId = process.env.ARYN_ELECTRON_DEBUG_RUN_ID
  ?? new Date().toISOString().replace(/[:.]/g, '-')
const runRoot = path.join(artifactRoot, 'runs', runId)
const logPath = path.join(artifactRoot, 'electron-debug-session.log')
const reportPath = path.join(artifactRoot, 'electron-debug-session-report.json')
const screenshotPath = path.join(artifactRoot, 'electron-debug-session.png')
const appDataRoot = path.join(runRoot, 'appdata')
const localAppDataRoot = path.join(runRoot, 'localappdata')
const tempRoot = path.join(runRoot, 'temp')
const defaultWorkspacePath = path.join(runRoot, 'workspace')
const defaultFilePath = path.join(defaultWorkspacePath, 'debug.md')

const timeoutMs = readNumberEnv('ARYN_ELECTRON_DEBUG_TIMEOUT_MS', 30_000)
const keepOpen = process.env.ARYN_ELECTRON_DEBUG_KEEP_OPEN === '1'
const keepOpenMs = readNumberEnv('ARYN_ELECTRON_DEBUG_KEEP_OPEN_MS', 10 * 60_000)
const viewMode = readViewMode(process.env.ARYN_ELECTRON_DEBUG_VIEW_MODE)
const debugScenario = readDebugScenario(process.env.ARYN_ELECTRON_DEBUG_SCENARIO)

function readNumberEnv(name, fallback) {
  const value = Number.parseInt(process.env[name] ?? '', 10)
  return Number.isFinite(value) && value > 0 ? value : fallback
}

function readViewMode(value) {
  return ['code', 'meo', 'preview'].includes(value ?? '') ? value : 'meo'
}

function readDebugScenario(value) {
  return ['agent-attachments', 'agent-mention-menu'].includes(value ?? '') ? value : null
}

function compactText(value, maxLength = 1200) {
  const text = String(value ?? '')
  const compacted = text.replace(/data:font\/woff2;base64,[^'")\s]+/g, 'data:font/woff2;base64,...')

  if (compacted.length <= maxLength) {
    return compacted
  }

  return `${compacted.slice(0, maxLength)}... [truncated ${compacted.length - maxLength} chars]`
}

function log(message, details) {
  const suffix = details ? ` ${JSON.stringify(details)}` : ''
  const line = `[electron-debug] ${message}${suffix}`
  console.log(line)
  try {
    fsSync.mkdirSync(artifactRoot, { recursive: true })
    fsSync.appendFileSync(logPath, `${line}\n`, 'utf8')
  } catch {
    // Diagnostics are best-effort and must not break the debug run.
  }
}

function serializeError(error) {
  if (error instanceof Error) {
    return {
      message: error.message,
      name: error.name,
      stack: error.stack,
    }
  }

  return {
    message: String(error),
    name: 'Error',
    stack: null,
  }
}

async function pathExists(targetPath) {
  try {
    await fs.access(targetPath)
    return true
  } catch {
    return false
  }
}

async function ensureBuildExists() {
  const missing = []

  if (!(await pathExists(mainEntry))) {
    missing.push(mainEntry)
  }

  if (!(await pathExists(rendererEntry))) {
    missing.push(rendererEntry)
  }

  if (missing.length > 0) {
    throw new Error(`Missing Electron build output: ${missing.join(', ')}. Run "npm.cmd run pretest" first, or use "npm.cmd run debug:electron:build".`)
  }
}

function createFixtureMarkdown() {
  return [
    '# Electron Debug Fixture',
    '',
    'This file is generated by scripts/electron-debug-session.mjs.',
    '',
    '- The script launches the real Electron app.',
    '- It restores this workspace and opens this Markdown file.',
    '- It captures renderer console messages, page errors, failed requests, screenshots, and a DOM snapshot.',
    '',
    'Use this as a stable starting point before debugging editor behavior.',
    '',
  ].join('\n')
}

async function resolveWorkspaceFixture() {
  const requestedWorkspacePath = process.env.ARYN_ELECTRON_DEBUG_WORKSPACE
    ? path.resolve(process.env.ARYN_ELECTRON_DEBUG_WORKSPACE)
    : null
  const requestedFilePath = process.env.ARYN_ELECTRON_DEBUG_FILE
    ? path.resolve(process.env.ARYN_ELECTRON_DEBUG_FILE)
    : null

  if ((requestedWorkspacePath && !requestedFilePath) || (!requestedWorkspacePath && requestedFilePath)) {
    throw new Error('Set both ARYN_ELECTRON_DEBUG_WORKSPACE and ARYN_ELECTRON_DEBUG_FILE, or set neither to use the generated fixture.')
  }

  if (requestedWorkspacePath && requestedFilePath) {
    return {
      created: false,
      filePath: requestedFilePath,
      workspacePath: requestedWorkspacePath,
    }
  }

  await fs.rm(defaultWorkspacePath, { force: true, recursive: true })
  await fs.mkdir(defaultWorkspacePath, { recursive: true })
  await fs.writeFile(defaultFilePath, createFixtureMarkdown(), 'utf8')

  if (debugScenario === 'agent-mention-menu') {
    await fs.mkdir(path.join(defaultWorkspacePath, 'docs'), { recursive: true })
    await fs.mkdir(path.join(defaultWorkspacePath, 'src', 'components'), { recursive: true })
    await fs.mkdir(path.join(defaultWorkspacePath, 'proto'), { recursive: true })
    await Promise.all([
      fs.writeFile(path.join(defaultWorkspacePath, 'AGENTS.md'), '# Agents\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'README.md'), '# Debug workspace\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'docs', 'uxspecs_agent.md'), '# UX Specs Agent\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'docs', 'feature-notes.md'), '# Feature notes\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'proto', 'pomodoro.html'), '<!doctype html>\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'src', 'components', 'agent-composer.tsx'), 'export {}\n', 'utf8'),
      fs.writeFile(path.join(defaultWorkspacePath, 'src', 'components', 'attachment-card.tsx'), 'export {}\n', 'utf8'),
    ])
  }

  return {
    created: true,
    filePath: requestedFilePath ?? defaultFilePath,
    workspacePath: requestedWorkspacePath ?? defaultWorkspacePath,
  }
}

async function resetArtifactRoots() {
  fsSync.mkdirSync(artifactRoot, { recursive: true })
  fsSync.writeFileSync(logPath, '', 'utf8')
  await fs.rm(appDataRoot, { force: true, recursive: true })
  await fs.rm(localAppDataRoot, { force: true, recursive: true })
  await fs.rm(tempRoot, { force: true, recursive: true })
  await fs.mkdir(appDataRoot, { recursive: true })
  await fs.mkdir(localAppDataRoot, { recursive: true })
  await fs.mkdir(tempRoot, { recursive: true })
}

function attachProcessCapture(app, report) {
  const processHandle = app.process()

  if (!processHandle) {
    return
  }

  report.process = {
    pid: processHandle.pid ?? null,
    stderr: [],
    stdout: [],
  }

  processHandle.stdout?.on('data', (chunk) => {
    const text = compactText(chunk.toString())
    report.process.stdout.push(text)
    log('main stdout', { text })
  })

  processHandle.stderr?.on('data', (chunk) => {
    const text = compactText(chunk.toString())
    report.process.stderr.push(text)
    log('main stderr', { text })
  })
}

function attachPageCapture(page, report) {
  page.on('console', (message) => {
    const entry = {
      location: message.location(),
      text: compactText(message.text()),
      type: message.type(),
    }
    report.renderer.console.push(entry)
    log('renderer console', entry)
  })

  page.on('pageerror', (error) => {
    const entry = serializeError(error)
    report.renderer.pageErrors.push(entry)
    log('renderer pageerror', { message: entry.message })
  })

  page.on('requestfailed', (request) => {
    const entry = {
      failure: request.failure()?.errorText ?? null,
      method: request.method(),
      resourceType: request.resourceType(),
      url: compactText(request.url()),
    }
    report.renderer.requestFailures.push(entry)
    log('request failed', entry)
  })

  page.on('response', (response) => {
    if (response.status() < 400) {
      return
    }

    const entry = {
      status: response.status(),
      url: compactText(response.url()),
    }
    report.renderer.httpErrors.push(entry)
    log('http error', entry)
  })

  page.on('crash', () => {
    report.renderer.crashed = true
    log('renderer crashed')
  })
}

async function waitForAppShell(page) {
  await page.waitForLoadState('domcontentloaded', { timeout: timeoutMs })
  await page.waitForSelector('.app-shell', { timeout: timeoutMs })
}

function tabStorageKey(workspacePath) {
  return `aryn:file-tabs:${encodeURIComponent(workspacePath)}`
}

async function restoreWorkspace(page, fixture) {
  log('waiting for preload appApi')
  await page.waitForFunction(() => !!window.appApi?.updateWorkspaceState, null, { timeout: timeoutMs })
  log('restoring workspace', {
    filePath: fixture.filePath,
    viewMode,
    workspacePath: fixture.workspacePath,
  })

  await page.evaluate(
    ({ debugScenario, filePath, storageKey, viewMode, workspacePath }) => {
      window.localStorage.setItem(storageKey, JSON.stringify({
        activePath: filePath,
        entries: [{ path: filePath, viewMode }],
        paths: [filePath],
      }))

      if (debugScenario === 'agent-attachments' || debugScenario === 'agent-mention-menu') {
        window.localStorage.setItem('aryn:settings', JSON.stringify({
          state: {
            layoutPreference: 'agent',
            meo: {
              focusedLineHighlight: false,
              gitDiffLineHighlights: true,
              imageFolder: 'assets',
              outlinePosition: 'right',
            },
            theme: 'auto',
          },
          version: 0,
        }))
        window.localStorage.setItem('aryn:left-sidebar-collapsed', 'true')
        window.localStorage.setItem('aryn:agent-right-sidebar-collapsed', 'true')
      } else {
        window.localStorage.setItem('aryn:left-sidebar-collapsed', 'false')
        window.localStorage.setItem('aryn:right-sidebar-collapsed', 'false')
      }

      return window.appApi.updateWorkspaceState(workspacePath, {
        lastFilePath: filePath,
        markAsLastOpened: true,
      })
    },
    {
      debugScenario,
      filePath: fixture.filePath,
      storageKey: tabStorageKey(fixture.workspacePath),
      viewMode,
      workspacePath: fixture.workspacePath,
    },
  )

  await page.reload({ waitUntil: 'domcontentloaded', timeout: timeoutMs })
  await waitForAppShell(page)
  if (debugScenario === 'agent-attachments' || debugScenario === 'agent-mention-menu') {
    await page.waitForSelector('.agent-composer-editor', { timeout: timeoutMs })
  } else {
    await page.waitForFunction(() => {
      return !!document.querySelector('.meo-native-root, .cm-content, .monaco-editor')
    }, null, { timeout: timeoutMs })
  }
  await page.waitForTimeout(500)
}

async function applyDebugScenario(page) {
  if (debugScenario !== 'agent-attachments' && debugScenario !== 'agent-mention-menu') {
    return null
  }

  log('applying debug scenario', { debugScenario })
  await page.waitForSelector('.agent-composer-editor', { timeout: timeoutMs })

  if (debugScenario === 'agent-mention-menu') {
    await page.click('.agent-composer-editor')
    await page.keyboard.type('@')
    await page.waitForSelector('.agent-composer-mention-menu', { timeout: timeoutMs })
    await page.waitForTimeout(500)

    return page.evaluate(() => {
      const readRect = (selector) => {
        const element = document.querySelector(selector)

        if (!element) {
          return null
        }

        const rect = element.getBoundingClientRect()
        return {
          bottom: Math.round(rect.bottom),
          height: Math.round(rect.height),
          left: Math.round(rect.left),
          right: Math.round(rect.right),
          top: Math.round(rect.top),
          width: Math.round(rect.width),
        }
      }

      const menu = document.querySelector('.agent-composer-mention-menu')
      const field = document.querySelector('.agent-composer-field')

      return {
        editorText: document.querySelector('.agent-composer-editor')?.textContent ?? '',
        field: {
          rect: readRect('.agent-composer-field'),
        },
        menu: {
          isInField: !!(menu && field?.contains(menu)),
          itemCount: document.querySelectorAll('.agent-composer-mention-option').length,
          rect: readRect('.agent-composer-mention-menu'),
        },
        textShell: {
          rect: readRect('.agent-composer-text-shell'),
        },
      }
    })
  }

  await page.evaluate(async () => {
    const editor = document.querySelector('.agent-composer-editor')
    if (!editor) {
      throw new Error('Agent composer editor not found.')
    }

    const createImageFile = async (name, accent) => {
      const canvas = document.createElement('canvas')
      canvas.width = 160
      canvas.height = 96
      const context = canvas.getContext('2d')
      if (!context) {
        throw new Error('Unable to create image attachment fixture.')
      }
      context.fillStyle = '#fafafa'
      context.fillRect(0, 0, canvas.width, canvas.height)
      context.fillStyle = '#e5e7eb'
      context.fillRect(10, 12, 140, 14)
      context.fillRect(10, 36, 90, 8)
      context.fillRect(10, 52, 124, 8)
      context.strokeStyle = accent
      context.lineWidth = 2
      context.beginPath()
      for (let x = 12; x < 150; x += 8) {
        const y = 82 - Math.sin(x / 9) * 14
        if (x === 12) {
          context.moveTo(x, y)
        } else {
          context.lineTo(x, y)
        }
      }
      context.stroke()
      const imageBlob = await new Promise((resolve, reject) => {
        canvas.toBlob((blob) => {
          if (blob) {
            resolve(blob)
            return
          }

          reject(new Error('Unable to create image attachment fixture.'))
        }, 'image/png')
      })

      return new File([imageBlob], name, { type: 'image/png' })
    }

    const imageFile = await createImageFile('Snipaste_2025-12-10_20-13-32.png', '#9ca3af')
    const secondImageFile = await createImageFile('light-dashboard-reference.png', '#cbd5e1')

    const dataTransfer = new DataTransfer()
    dataTransfer.items.add(imageFile)
    dataTransfer.items.add(secondImageFile)
    editor.dispatchEvent(new DragEvent('drop', {
      bubbles: true,
      cancelable: true,
      dataTransfer,
    }))
  })

  await page.waitForFunction(() => {
    return document.querySelectorAll('.agent-composer-attachments .agent-attachment-item').length >= 2
  }, null, { timeout: timeoutMs })
  await page.click('.agent-composer-editor')
  await page.keyboard.type('Myprompt')
  await page.waitForTimeout(500)

  return page.evaluate(() => {
    const readRect = (selector) => {
      const element = document.querySelector(selector)

      if (!element) {
        return null
      }

      const rect = element.getBoundingClientRect()
      return {
        bottom: Math.round(rect.bottom),
        height: Math.round(rect.height),
        left: Math.round(rect.left),
        right: Math.round(rect.right),
        top: Math.round(rect.top),
        width: Math.round(rect.width),
      }
    }

    const field = document.querySelector('.agent-composer-field')
    const fieldStyle = field ? getComputedStyle(field) : null

    return {
      attachments: {
        count: document.querySelectorAll('.agent-composer-attachments .agent-attachment-item').length,
        imagePreviewCount: document.querySelectorAll('.agent-composer-attachments .agent-attachment-preview.has-image img').length,
        imageItemStyle: (() => {
          const item = document.querySelector('.agent-composer-attachments .agent-attachment-item.is-image')
          const style = item ? getComputedStyle(item) : null
          return style
            ? {
                borderBottomColor: style.borderBottomColor,
                borderBottomStyle: style.borderBottomStyle,
                borderBottomWidth: style.borderBottomWidth,
                boxShadow: style.boxShadow,
              }
            : null
        })(),
        rect: readRect('.agent-composer-attachments'),
      },
      editorText: document.querySelector('.agent-composer-editor')?.textContent ?? '',
      field: {
        rect: readRect('.agent-composer-field'),
        style: fieldStyle
          ? {
              display: fieldStyle.display,
              flexDirection: fieldStyle.flexDirection,
              gridTemplateRows: fieldStyle.gridTemplateRows,
            }
          : null,
      },
      footer: {
        rect: readRect('.agent-composer-meta'),
      },
      placeholder: {
        rect: readRect('.agent-composer-placeholder'),
      },
      shell: {
        appLayout: document.querySelector('.app-shell')?.getAttribute('data-app-layout') ?? null,
        leftCollapsed: document.querySelector('.app-shell')?.getAttribute('data-left-collapsed') ?? null,
        rightCollapsed: document.querySelector('.app-shell')?.getAttribute('data-right-collapsed') ?? null,
        rect: readRect('.app-shell'),
      },
      textShell: {
        rect: readRect('.agent-composer-text-shell'),
      },
    }
  })
}

async function snapshotPage(page) {
  return page.evaluate(() => {
    const readRect = (selector) => {
      const element = document.querySelector(selector)

      if (!element) {
        return null
      }

      const rect = element.getBoundingClientRect()
      return {
        height: Math.round(rect.height),
        left: Math.round(rect.left),
        top: Math.round(rect.top),
        width: Math.round(rect.width),
      }
    }

    const shell = document.querySelector('.app-shell')
    const activeTab = document.querySelector('.file-tab[data-active="true"], [data-active="true"].file-tab')
    const editor = document.querySelector('.meo-native-root, .cm-content, .monaco-editor')

    return {
      appApiAvailable: !!window.appApi,
      bodyTextSample: document.body.textContent?.slice(0, 500) ?? '',
      editor: {
        rect: readRect('.meo-native-root, .cm-content, .monaco-editor'),
        selectorMatched: editor
          ? [
              '.meo-native-root',
              '.cm-content',
              '.monaco-editor',
            ].find((selector) => editor.matches(selector)) ?? editor.tagName.toLowerCase()
          : null,
        textLength: editor?.textContent?.length ?? null,
      },
      shell: shell
        ? {
            leftCollapsed: shell.getAttribute('data-left-collapsed'),
            layout: shell.getAttribute('data-layout'),
            rect: readRect('.app-shell'),
            rightCollapsed: shell.getAttribute('data-right-collapsed'),
          }
        : null,
      tabCount: document.querySelectorAll('.file-tab').length,
      title: document.title,
      url: window.location.href,
      activeTabText: activeTab?.textContent?.trim() ?? null,
    }
  })
}

async function closeElectronApp(app) {
  let processHandle = null

  try {
    processHandle = app.process()
  } catch {
    processHandle = null
  }

  let closed = false

  await Promise.race([
    app.close()
      .then(() => {
        closed = true
      })
      .catch(() => {
        closed = true
      }),
    new Promise((resolve) => setTimeout(resolve, 5_000)),
  ])

  if (!closed && processHandle && !processHandle.killed) {
    processHandle.kill()
  }
}

async function keepOpenUntilClosed(app) {
  log('keeping electron open', { keepOpenMs })
  await Promise.race([
    app.waitForEvent('close').catch(() => undefined),
    new Promise((resolve) => setTimeout(resolve, keepOpenMs)),
  ])
}

async function main() {
  await resetArtifactRoots()
  log('starting', {
    artifactRoot,
    keepOpen,
    timeoutMs,
    viewMode,
  })

  await ensureBuildExists()
  const fixture = await resolveWorkspaceFixture()

  const report = {
    artifacts: {
      logPath,
      reportPath,
      screenshotPath,
    },
    generatedAt: new Date().toISOString(),
    ok: false,
    paths: {
      appDataRoot,
      artifactRoot,
      localAppDataRoot,
      mainEntry,
      rendererEntry,
      rootDir,
      runRoot,
      tempRoot,
    },
    process: {
      pid: null,
      stderr: [],
      stdout: [],
    },
    renderer: {
      console: [],
      crashed: false,
      httpErrors: [],
      pageErrors: [],
      requestFailures: [],
    },
    snapshot: {
      afterRestore: null,
      beforeRestore: null,
    },
    workspace: {
      ...fixture,
      debugScenario,
      viewMode,
    },
  }

  let app = null
  let page = null

  try {
    log('launching electron app root', { rootDir })
    app = await electron.launch({
      args: [rootDir],
      env: {
        ...process.env,
        APPDATA: appDataRoot,
        ARYN_ELECTRON_DEBUG: '1',
        ELECTRON_ENABLE_LOGGING: '1',
        LOCALAPPDATA: localAppDataRoot,
        TEMP: tempRoot,
        TMP: tempRoot,
      },
      timeout: timeoutMs,
    })
    attachProcessCapture(app, report)

    page = await app.firstWindow()
    attachPageCapture(page, report)
    await waitForAppShell(page)
    report.snapshot.beforeRestore = await snapshotPage(page)
    await restoreWorkspace(page, fixture)
    report.snapshot.afterRestore = await snapshotPage(page)
    report.snapshot.afterScenario = await applyDebugScenario(page)
    await page.screenshot({ path: screenshotPath, fullPage: false })

    report.ok = true
    log('debug session ready', {
      consoleMessages: report.renderer.console.length,
      pageErrors: report.renderer.pageErrors.length,
      requestFailures: report.renderer.requestFailures.length,
      screenshotPath,
    })

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2), 'utf8')

    if (keepOpen) {
      await keepOpenUntilClosed(app)
    }
  } catch (error) {
    report.error = serializeError(error)
    log('debug session failed', { message: report.error.message })

    if (page) {
      try {
        await page.screenshot({ path: screenshotPath, fullPage: false })
      } catch {
        // Ignore screenshot failure during fatal startup issues.
      }

      try {
        report.snapshot.afterFailure = await snapshotPage(page)
      } catch {
        report.snapshot.afterFailure = null
      }
    }

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2), 'utf8')
    process.exitCode = 1
  } finally {
    if (app && !keepOpen) {
      await closeElectronApp(app)
    }
  }

  log('report written', { ok: report.ok, reportPath })
  console.log(JSON.stringify({
    ok: report.ok,
    reportPath,
    renderer: {
      consoleMessages: report.renderer.console.length,
      pageErrors: report.renderer.pageErrors.length,
      requestFailures: report.renderer.requestFailures.length,
    },
    screenshotPath,
    workspace: report.workspace,
  }, null, 2))
}

main().catch((error) => {
  log('fatal error', serializeError(error))
  process.exitCode = 1
})
