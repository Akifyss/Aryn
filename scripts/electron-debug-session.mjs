function assertDebugScenarioResult(result) {
  if (!debugScenario) return
  if (!result) {
    throw new Error(`Debug scenario "${debugScenario}" did not return diagnostics.`)
  }

  if (debugScenario === 'agent-mention-menu') {
    const fieldRect = result.field?.rect
    const menuRect = result.menu?.rect
    const textShellRect = result.textShell?.rect
    const horizontalOverlap = menuRect && fieldRect
      ? Math.max(0, Math.min(menuRect.right, fieldRect.right) - Math.max(menuRect.left, fieldRect.left))
      : 0
    const isAnchoredAboveComposer = !!(
      menuRect
      && textShellRect
      && menuRect.top < textShellRect.top
      && menuRect.bottom <= textShellRect.top + 8
      && horizontalOverlap >= Math.min(menuRect.width, fieldRect?.width ?? 0) / 2
    )
    if (
      !result.editorText.includes('@')
      || !result.editor?.focused
      || !result.menu?.hasListbox
      || result.menu.activeItemCount !== 1
      || result.menu.itemCount < 1
      || !menuRect
      || menuRect.width <= 0
      || menuRect.height <= 0
      || !isAnchoredAboveComposer
    ) {
      throw new Error(`Agent mention menu scenario failed: ${JSON.stringify(result)}`)
    }
    return
  }

  if (debugScenario === 'agent-attachments') {
    if (
      result.attachments?.count !== 2
      || result.attachments.imagePreviewCount !== 2
      || !result.editorText.includes('Myprompt')
    ) {
      throw new Error(`Agent attachment scenario failed: ${JSON.stringify(result)}`)
    }
    return
  }

  if (
    !result.surfaceStayedMounted
    || result.officialSurfaceCount !== 1
    || result.legacyToolDetails !== 0
  ) {
    throw new Error(`OpenCode surface scenario failed: ${JSON.stringify(result)}`)
  }
}

async function cleanupDebugScenario(page) {
  if (debugScenario !== 'opencode-surface') return
  try {
    await page.evaluate(() => {
      window.__arynDebugOpenCodeSyntheticDispose?.()
    })
    log('cleaned up OpenCode synthetic surface')
  } catch (error) {
    log('failed to clean up OpenCode synthetic surface', serializeError(error))
  }
}

async function snapshotPage(page) {
  return page.evaluate(() => {
    const readRect = (selector) => {
      const element = document.querySelector(selector)

      if (!element) {
        return null
      }

      const rect = element.getBoundingClientRect()
      return {
        height: Math.round(rect.height),
        left: Math.round(rect.left),
        top: Math.round(rect.top),
        width: Math.round(rect.width),
      }
    }

    const shell = document.querySelector('.app-shell')
    const activeTab = document.querySelector('.file-tab[data-active="true"], [data-active="true"].file-tab')
    const editor = document.querySelector('.meo-native-root, .cm-content, .monaco-editor')

    return {
      appApiAvailable: !!window.appApi,
      bodyTextSample: document.body.textContent?.slice(0, 500) ?? '',
      editor: {
        rect: readRect('.meo-native-root, .cm-content, .monaco-editor'),
        selectorMatched: editor
          ? [
              '.meo-native-root',
              '.cm-content',
              '.monaco-editor',
            ].find((selector) => editor.matches(selector)) ?? editor.tagName.toLowerCase()
          : null,
        textLength: editor?.textContent?.length ?? null,
      },
      shell: shell
        ? {
            leftCollapsed: shell.getAttribute('data-left-collapsed'),
            layout: shell.getAttribute('data-layout'),
            rect: readRect('.app-shell'),
            rightCollapsed: shell.getAttribute('data-right-collapsed'),
          }
        : null,
      tabCount: document.querySelectorAll('.file-tab').length,
      title: document.title,
      url: window.location.href,
      activeTabText: activeTab?.textContent?.trim() ?? null,
    }
  })
}

async function closeElectronApp(app) {
  let processHandle = null

  try {
    processHandle = app.process()
  } catch {
    processHandle = null
  }

  let closed = false

  await Promise.race([
    app.close()
      .then(() => {
        closed = true
      })
      .catch(() => {
        closed = true
      }),
    new Promise((resolve) => setTimeout(resolve, 5_000)),
  ])

  if (!closed && processHandle && !processHandle.killed) {
    processHandle.kill()
  }
}

async function keepOpenUntilClosed(app) {
  log('keeping electron open', { keepOpenMs })
  await Promise.race([
    app.waitForEvent('close').catch(() => undefined),
    new Promise((resolve) => setTimeout(resolve, keepOpenMs)),
  ])
}

async function main() {
  await resetArtifactRoots()
  log('starting', {
    artifactRoot,
    keepOpen,
    timeoutMs,
    viewMode,
  })

  await ensureBuildExists()
  const fixture = await resolveWorkspaceFixture()

  const report = {
    artifacts: {
      logPath,
      reportPath,
      screenshotPath,
    },
    generatedAt: new Date().toISOString(),
    ok: false,
    paths: {
      appDataRoot,
      artifactRoot,
      homeRoot,
      localAppDataRoot,
      mainEntry,
      rendererEntry,
      rootDir,
      runRoot,
      tempRoot,
      userDataRoot,
    },
    process: {
      pid: null,
      stderr: [],
      stdout: [],
    },
    renderer: {
      console: [],
      crashed: false,
      httpErrors: [],
      pageErrors: [],
      requestFailures: [],
    },
    snapshot: {
      afterRestore: null,
      beforeRestore: null,
    },
    workspace: {
      ...fixture,
      debugScenario,
      viewMode,
    },
  }

  let app = null
  let page = null

  try {
    log('launching electron app root', { rootDir })
    app = await electron.launch({
      args: [`--user-data-dir=${userDataRoot}`, rootDir],
      env: {
        ...process.env,
        APPDATA: appDataRoot,
        ARYN_ELECTRON_DEBUG: '1',
        ARYN_ELECTRON_DEBUG_HOME: homeRoot,
        ARYN_ELECTRON_DEBUG_WORKSPACE: fixture.workspacePath,
        ELECTRON_ENABLE_LOGGING: '1',
        LOCALAPPDATA: localAppDataRoot,
        TEMP: tempRoot,
        TMP: tempRoot,
      },
      timeout: timeoutMs,
    })
    attachProcessCapture(app, report)

    page = await app.firstWindow()
    attachPageCapture(page, report)
    await waitForAppShell(page)
    report.snapshot.beforeRestore = await snapshotPage(page)
    await restoreWorkspace(page, fixture)
    report.snapshot.afterRestore = await snapshotPage(page)
    report.snapshot.afterScenario = await applyDebugScenario(page)
    assertDebugScenarioResult(report.snapshot.afterScenario)
    if (debugScenario && (report.renderer.crashed || report.renderer.pageErrors.length > 0)) {
      throw new Error(`Renderer failed during "${debugScenario}": ${JSON.stringify({
        crashed: report.renderer.crashed,
        pageErrors: report.renderer.pageErrors,
      })}`)
    }
    await page.screenshot({ path: screenshotPath, fullPage: false })

    report.ok = true
    log('debug session ready', {
      consoleMessages: report.renderer.console.length,
      pageErrors: report.renderer.pageErrors.length,
      requestFailures: report.renderer.requestFailures.length,
      screenshotPath,
    })

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2), 'utf8')

    if (keepOpen) {
      await keepOpenUntilClosed(app)
    }
  } catch (error) {
    report.error = serializeError(error)
    log('debug session failed', { message: report.error.message })

    if (page) {
      try {
        await page.screenshot({ path: screenshotPath, fullPage: false })
      } catch {
        // Ignore screenshot failure during fatal startup issues.
      }

      try {
        report.snapshot.afterFailure = await snapshotPage(page)
      } catch {
        report.snapshot.afterFailure = null
      }
    }

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2), 'utf8')
    process.exitCode = 1
  } finally {
    if (page) {
      await cleanupDebugScenario(page)
    }
    if (app && !keepOpen) {
      await closeElectronApp(app)
    }
  }

  log('report written', { ok: report.ok, reportPath })
  console.log(JSON.stringify({
    ok: report.ok,
    reportPath,
    renderer: {
      consoleMessages: report.renderer.console.length,
      pageErrors: report.renderer.pageErrors.length,
      requestFailures: report.renderer.requestFailures.length,
    },
    screenshotPath,
    workspace: report.workspace,
  }, null, 2))
}

main().catch((error) => {
  log('fatal error', serializeError(error))
  process.exitCode = 1
})
